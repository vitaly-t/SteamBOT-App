﻿
CREATE TABLE CSGO_GROUPS
(
    STEAM_GROUP_ID_64 NUMERIC PRIMARY KEY NOT NULL,
    STEAM_GROUP_NAME  VARCHAR(50) NOT NULL,
    SITE_URL  	      VARCHAR(100) NOT NULL
);

CREATE TABLE CSGO_USERS
(
    USER_ID     	  SERIAL PRIMARY KEY,
    STEAM_ID_64 	  NUMERIC NOT NULL,
    STEAM_ID    	  VARCHAR(50) NOT NULL,
    STEAM_GROUP_ID_64 NUMERIC NOT NULL REFERENCES CSGO_GROUPS (STEAM_GROUP_ID_64)
);

